#' @export
knob <- function(title, value, min, max, width = NULL, height = NULL, ...){
  params = list(title = title, value = value, min = min, max = max,
    width = width, height = height, ...               
  )
  params = Filter(Negate(is.null), params)
  structure(params,
    class = c('knob', 'htmlwidget'),
    fillViewer = TRUE
  )
}

#' @export
widget_html.knob <- function(x, id, style, class){
  tags$input(type = "text", class = class, id = id, `data-readOnly` = TRUE)
}

#' @export
knobOutput <- htmlwidgets::widgetOutput('knob')


